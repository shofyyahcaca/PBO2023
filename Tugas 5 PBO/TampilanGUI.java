    private int masukkanData(Connection conn, String id_karyawan, String nama, String alamat) throws SQLException {
        PreparedStatement pst = (PreparedStatement) conn.prepareStatement("INSERT INTO identitas (id_karyawan,nama,alamat) VALUES(?,?,?)");
        pst.setString(1, id_karyawan);
        pst.setString(2, nama);
        pst.setString(3, alamat);
        return pst.executeUpdate();
    }
    
    private int perbaruiData(Connection conn, String id_karyawan, String nama, String alamat) throws SQLException {
        PreparedStatement pst = (PreparedStatement) conn.prepareStatement("UPDATE identitas SET nama=?, alamat=? WHERE id_karyawan=?");
        pst.setString(1, nama);
        pst.setString(2, alamat);
        pst.setString(3, id_karyawan);
        return pst.executeUpdate();
    }

    private int hapusData(Connection conn, String id_karyawan) throws SQLException {
        PreparedStatement pst = (PreparedStatement) conn.prepareStatement("DELETE FROM identitas WHERE id_karyawan=?");
        pst.setString(1, id_karyawan);
        return pst.executeUpdate();
    }


    private void tampil(Connection conn) {
        dataKaryawan.clear();
        try {
            String sql = "select * from identitas";
            Statement st = (Statement) conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Karyawan data = new Karyawan();
                data.setId_karyawan(String.valueOf(rs.getObject(1)));
                data.setNama(String.valueOf(rs.getObject(2)));
                data.setAlamat(String.valueOf(rs.getObject(3)));
                dataKaryawan.add(data);
            }
            DefaultTableModel model = (DefaultTableModel) jTableKaryawan.getModel();
            model.setRowCount(0);
            for (Karyawan data : dataKaryawan) {

                Object[] baris = new Object[3];
                baris[0] = data.getId_karyawan();
                baris[1] = data.getNama();
                baris[2] = data.getAlamat();

                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

