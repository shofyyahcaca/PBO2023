    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String id_karyawan = jTextFieldIdKaryawan.getText().trim();
        String nama = jTextFieldNama.getText();
        String alamat = jTextFieldAlamat.getText();
        if (!id_karyawan.isEmpty() && !nama.isEmpty() && !alamat.isEmpty()) {

            try {
                // TODO add your handling code here:
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/karyawan", "postgres", "1234");
                int k = masukkanData(conn, id_karyawan, nama, alamat);
                if (k > 0) {
                    tampil(conn);
                    this.peringatan("Simpan Berhasil");
                } else {
                    this.peringatan("Simpan Gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.peringatan("Input Data Karyawan Gagal");
        }
        jTextFieldAlamat.setText("");
        jTextFieldIdKaryawan.setText("");
        jTextFieldNama.setText("");

    }//GEN-LAST:event_jButtonSimpanActionPerformed

